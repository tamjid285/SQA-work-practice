package Java2;

public class ArithmeticOperator {
    public static void main(String[] args) {
        int a= 10, b=20;
        double sum = a + b;
        double difference = a - b;
        double product = a * b;
        double quotient = a / b;
        System.out.println(sum);
        System.out.println(difference);
        System.out.println(product);
        System.out.println(quotient);
    }
}
